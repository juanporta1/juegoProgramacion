def draw(maps):
    display = ""
    for x in maps:
        display += "                               "
        for y in x:
            if y == 1:
                display += Fore.MAGENTA + "# "
            elif y == 2:
                display += Fore.GREEN + "X "
            elif y == 4:
                display += Fore.RED + "@ "
            elif y == 0:
                display += "  "
        display += "\n"
    os.system("cls")
    print(display)

